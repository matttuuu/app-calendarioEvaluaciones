package com.example.calendarapp_idra;

public class EventsIngles {

    String EVENTING, TIMEING, DATEING, MONTHING, YEARING;

    public String getEVENTING() {
        return EVENTING;
    }

    public void setEVENTING(String EVENT) {
        this.EVENTING = EVENTING;
    }

    public String getTIMEING() {
        return TIMEING;
    }

    public void setTIMEING(String TIME) {
        this.TIMEING = TIMEING;
    }

    public String getDATEING() {
        return DATEING;
    }

    public void setDATEING(String DATE) {
        this.DATEING = DATEING;
    }

    public String getMONTHING() {
        return MONTHING;
    }

    public void setMONTHING(String MONTH) {
        this.MONTHING = MONTHING;
    }

    public String getYEARING() {
        return YEARING;
    }

    public void setYEARING(String YEAR) {
        this.YEARING = YEARING;
    }

    public EventsIngles(String EVENTING, String TIMEING, String DATEING, String MONTHING, String YEARING) {
        this.EVENTING = EVENTING;
        this.TIMEING = TIMEING;
        this.DATEING = DATEING;
        this.MONTHING = MONTHING;
        this.YEARING = YEARING;
    }
}
